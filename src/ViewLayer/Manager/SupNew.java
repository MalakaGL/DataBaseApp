    private void btnAddTPSupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTPSupActionPerformed
        // TODO add your handling code here:
        if (numbers.isEmpty()) {
            JTextField temp = new JTextField("");
            temp.setPreferredSize(txtNewSupName.getSize());
            numbers.add(temp);
            pnlAddTPSup.add(temp);
            pnlAddTPSup.revalidate();
            validate();
        } else if (!((JTextField) numbers.get(numbers.size() - 1)).getText().equals("")) {
            JTextField temp = new JTextField("");
            temp.setPreferredSize(txtNewSupAddStrt.getSize());
            numbers.add(temp);
            pnlAddTPSup.add(temp);
            pnlAddTPSup.revalidate();
            validate();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter the number above.");
        }
    }//GEN-LAST:event_btnAddTPSupActionPerformed

