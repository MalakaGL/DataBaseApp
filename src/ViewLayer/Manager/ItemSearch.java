    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        // TODO add your handling code here:

        Object[] conditions = new Object[12];
        switch (cmbSearchItmBy.getSelectedIndex()) {
            case 0:
            JOptionPane.showMessageDialog(this, "Please select a value "
                + "type to find.");
            return;

            case 1:
            conditions[0] = "ItemCode";
            break;
            case 2:
            conditions[0] = "ItemTypeName";
            break;
            case 3:
            conditions[0] = "BrandName";
            break;
            case 4:
            conditions[0] = "AvailableQty";
            break;
            case 5:
            conditions[0] = "BuyingPrice";
            break;
            case 6:
            conditions[0] = "SellingPrice";
            break;
            case 7:
            conditions[0] = "Size";
            break;
        }
        conditions[1] = txtSearchVal1.getText();

        conditions[2] = chkBoxItemCode.isSelected();
        conditions[3] = chkBoxItemTypeName.isSelected();
        conditions[4] = chkBoxBrandName.isSelected();
        conditions[5] = chkBoxBPrc1.isSelected();
        conditions[6] = chkBoxSPrc.isSelected();
        conditions[7] = chkBoxLID.isSelected();
        conditions[8] = chkBoxLBQty.isSelected();
        conditions[9] = chkBoxAvlQty1.isSelected();
        conditions[10] = chkBoxDes.isSelected();
        conditions[11]=chkBoxSize.isSelected();

        JTable table = StoreKeeperController.getTable(conditions);

        if (table == null || table.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No result found.");
        }
        ItemDetails.setModel(table.getModel());
    }//GEN-LAST:event_jButton14ActionPerformed

